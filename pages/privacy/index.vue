        opgeslagen. Zodra je de webbrowser sluit, worden deze cookies automatisch verwijderd. Deze zogenaamde sessie
        cookies worden gebruikt om jouw surfgedrag op de Website tijdens een bepaald bezoek in kaart te brengen.
